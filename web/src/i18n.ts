export function t(locale: Locale, key: string, params?: Record<string, string | number>): string {
  const template = MESSAGES[locale][key] ?? MESSAGES["en-US"][key] ?? key;
  if (!params) {
    return template;
  }
  return template.replace(/\{(\w+)\}/g, (_, token: string) => String(params[token] ?? ""));
}

export function formatDateTime(locale: Locale, value: string | number | null | undefined): string {
  if (!value) {
    return "-";
  }
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) {
    return String(value);
  }
  return date.toLocaleString(locale);
}

export function translateStatus(locale: Locale, value: string | null | undefined): string {
  const normalized = value?.trim() || "unknown";
  return t(locale, `status.${normalized}`);
}

export function translateSaveState(locale: Locale, value: string): string {
  return t(locale, `save.${value}`);
}

export function translateBoolean(locale: Locale, value: boolean): string {
  return t(locale, value ? "common.yes" : "common.no");
}
